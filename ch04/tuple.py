tuple1 = (10, "yellow", 0.5, [3, 2, 1]) # 튜플 생성
print(tuple1)
print(type(tuple1))

print("-------------------------------------")


tuple2 = () # 빈 튜플 생성
print(tuple2)
print(type(tuple2))

print("-------------------------------------")

tuple3 = 10, "yellow", 0.5, [3, 2, 1] # 괄호 없이 튜플 생성
print(tuple3)
print(type(tuple3))

print("-------------------------------------")

tuple4 = (1,) # 요소 1개인 튜플 생성
print(tuple4)
print(type(tuple4))

tuple4 = (1)
print(type(tuple4)) # int

print("-------------------------------------")

tuple5 = 1, # 괄호 없고 요소 1개인 튜플 생성
print(tuple5)
print(type(tuple5))